    public ArrayList<LecturersTime> GetLecturersList()
    {
        ArrayList<LecturersTime> list = new ArrayList<LecturersTime>();
        try{
        connection = DBconnection.getConnection();
        String querry = "select * from sp2_lecturers_wise";
        Statement st;
        ResultSet rs;
        
        st = connection.createStatement();
        rs= st.executeQuery(querry);
        LecturersTime LEC;
        while(rs.next())
        {
            LEC = new LecturersTime(rs.getInt("id"), rs.getString("lecName"), rs.getInt("lecId"), rs.getString("day"), rs.getString("time_from"), rs.getString("time_to"));
            list.add(LEC);
        }
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        return list;
    }
    
    public void showLecturersList()
    {
        ArrayList<LecturersTime> list = GetLecturersList();
        DefaultTableModel model = (DefaultTableModel)tbl_LecTime.getModel();
        Object[] row = new Object[5];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getLecId();
            row[2] = list.get(i).getLecName();
            row[3] = list.get(i).getday();
            row[4] = list.get(i).gettime_from()+" - "+list.get(i).gettime_to();
            
            model.addRow(row);
        }
    }
            
